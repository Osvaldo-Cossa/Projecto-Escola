    private void efectuarAvalBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_efectuarAvalBtnActionPerformed
        try {
            DisciplinaController dControl = new DisciplinaController();
            Avaliacao avaliacao = new Avaliacao(JOptionPane.showInputDialog("Nome da Avaliacao: "), 
            new Date(System.currentTimeMillis()).toString(), dControl.codigoDisciplina(Login.CODIGO_DO_PROFESSOR), Login.CODIGO_DO_PROFESSOR);
            AvaliacaoController control = new AvaliacaoController();
            if(control.save(avaliacao)){
                EstudanteController eControl = new EstudanteController();
                for (int i = 0; i<eControl.showAll().size(); i++){
                    NotaController nControl = new NotaController();
                    Nota nota = new Nota(Double.parseDouble(JOptionPane.showInputDialog("Nota do Estudante: "+eControl.showAll().get(i).getNome())),
                    eControl.showAll().get(i).getNome(), dControl.codigoDisciplina(Login.CODIGO_DO_PROFESSOR),Login.CODIGO_DO_PROFESSOR);
                    nControl.createTable();
                    if(!nControl.save(nota)){
                        JOptionPane.showMessageDialog(null, "ERRO");break;
                    }
                }
                JOptionPane.showMessageDialog(null, "Notas Registadas");
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(TeacherDashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_efectuarAvalBtnActionPerformed

