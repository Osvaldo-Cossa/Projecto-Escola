    public void fillAvalTable(){
        try {
            DefaultTableModel modelo = (DefaultTableModel)avalTable.getModel();
            modelo.setNumRows(0);
            AvaliacaoController control = new AvaliacaoController(); 
            NotaController nControl = new NotaController();
            for(int i = 0; i < nControl.ListAll(Login.CODIGO_DO_PROFESSOR).size(); i++){
                String data[] = {control.ListTable(Login.CODIGO_DO_PROFESSOR).get(0).getNomeAvaliacao(),
                control.ListTable(Login.CODIGO_DO_PROFESSOR).get(0).getData(),
                control.ListTable(Login.CODIGO_DO_PROFESSOR).get(0).getNomeDisciplina(),
                String.valueOf(nControl.ListAll(Login.CODIGO_DO_PROFESSOR).get(i).getNota()),
                nControl.ListAll(Login.CODIGO_DO_PROFESSOR).get(i).getNomeEstudante()};
                DefaultTableModel model = (DefaultTableModel)avalTable.getModel();
                model.addRow(data);
            }
        } catch (ClassNotFoundException | SQLException ex) {
            Logger.getLogger(Dashboard.class.getName()).log(Level.SEVERE, null, ex);
        }
    }
    
